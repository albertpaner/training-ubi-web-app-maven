package model.Dto;

public class ParagrafoDto {

	int idSezione;
	String parTitolo;
	String parDescr;
	
	public ParagrafoDto() {}

	public int getIdSezione() {
		return idSezione;
	}

	public void setIdSezione(int idSezione) {
		this.idSezione = idSezione;
	}

	public String getParTitolo() {
		return parTitolo;
	}

	public void setParTitolo(String parTitolo) {
		this.parTitolo = parTitolo;
	}

	public String getParDescr() {
		return parDescr;
	}

	public void setParDescr(String parDescr) {
		this.parDescr = parDescr;
	};
	
	
	
}
